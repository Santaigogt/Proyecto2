    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (!txtId.getText().isEmpty() && !txtNombre.getText().isEmpty()) {
            String id = txtId.getText();
            String nombre = txtNombre.getText();
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate fechaNac = null;
            try {
                fechaNac = LocalDate.parse(txtFechNac.getText(), formatter);
            } catch (DateTimeParseException e) {
                JOptionPane.showMessageDialog(this, "Fecha incorrecta. Por favor, ingrese la fecha en el formato dd/MM/yyyy.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            String tel = txtTel.getText();
            String correo = txtCorreo.getText();
            cliente = new Cliente(id, nombre, fechaNac, correo, tel);
            list.agregar(cliente);
            mostrarTabla();
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese los valores nesesarios para agregar al cliente");
        }
        txtId.setText("");
        txtNombre.setText("");
        txtFechNac.setText("");
        txtCorreo.setText("");
        txtTel.setText("");
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblCliente.getModel();
        int fila = tblCliente.getSelectedRow();
        if (tblCliente.getSelectedColumnCount() == 1) {
            int resp = JOptionPane.showConfirmDialog(this, "Â¿Desea borrar el resgitro?");
            if (resp == 0) {  //El usuario quiere eliminar, Respuesta si
                String id = model.getValueAt(fila, 0).toString();
                if (list.Eliminar(id)) {
                    mostrarTabla();
                } else {
                    JOptionPane.showMessageDialog(null, "No se encontro un Cliente");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una fila...");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblCliente.getModel();
        int fila = tblCliente.getSelectedRow();
        if (tblCliente.getSelectedColumnCount() == 1) {
            String id = model.getValueAt(fila, 0).toString();
            list.Actualizar(id, txtTel.getText(), txtCorreo.getText());
            mostrarTabla();
            txtTel.setText("");
            txtCorreo.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una fila...");
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void txtBIdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBIdKeyReleased
        cliente = new Cliente();
        DefaultTableModel model;
        String titulos[] = {"Id", "Nombre", "Fecha Nac", "Telefono", "Correo", "Edad"};
        model = new DefaultTableModel(null, titulos);
        String id = txtBId.getText();
        cliente = list.Buscar(id);
        if (list.Buscar(id) != null) {
            Object newFila[] = {cliente.getId(),
                cliente.getNombre(), cliente.getFechaNacimiento(),
                cliente.getTelefono(), cliente.getCorreo(), cliente.obtenerEdad()};
            model.addRow(newFila);
        }
        tblCliente.setModel(model);


    }//GEN-LAST:event_txtBIdKeyReleased

    private void mostrarTabla() {
        cliente = new Cliente();
        DefaultTableModel model;
        String titulos[] = {"Id", "Nombre", "Fecha Nac", "Telefono", "Correo", "Edad"};
        model = new DefaultTableModel(null, titulos);
        for (int i = 0; i < list.getClientes().size(); i++) {
            cliente = list.getClientes().get(i);
            Object nuevaFila[] = {cliente.getId(), cliente.getNombre(),
                cliente.getFechaNacimiento(), cliente.getTelefono(), cliente.getCorreo(), cliente.obtenerEdad()};
            model.addRow(nuevaFila);
        }
        tblCliente.setModel(model);

    }
    private void txtBNombreKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBNombreKeyReleased
        cliente = new Cliente();
        DefaultTableModel model;
        String titulos[] = {"Id", "Nombre", "Fecha Nac", "Telefono", "Correo", "Edad"};
        model = new DefaultTableModel(null, titulos);
        for (int i = 0; i < list.getClientes().size(); i++) {
            cliente = list.getClientes().get(i);
            if (cliente.getNombre().toLowerCase().contains(txtBNombre.getText().toLowerCase())) {
                Object nuevaFila[] = {cliente.getId(), cliente.getNombre(),
                    cliente.getFechaNacimiento(), cliente.getTelefono(), cliente.getCorreo(), cliente.obtenerEdad()};
                model.addRow(nuevaFila);
            } 
        }
        tblCliente.setModel(model);

    }//GEN-LAST:event_txtBNombreKeyReleased


